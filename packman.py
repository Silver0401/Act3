def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()
    #dibuja el cuadrado con un for loop de 4!
    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

# funcion para que todo se vea bien
def offset(point):
    "Return offset of point in tiles."
    #utiliza un floor para poder tener un minimo!
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

#verificar que los fatnasmas y packman esten en una posicion valida!
def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)
    #verificar que el index del personaje no sea el 0 verificar con la linea 21 de tiles!!
    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')
    #dibuja todo el mapa con un for loop de los tiles, ver linea 21  de tiles!!
    for index in range(len(tiles)):
        tile = tiles[index]
        #los valores de 1 son el mapa que puede navegar el usuraio y los fantasmas!!
        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')
#El movimiento de jugador y los fantasmas
def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    #movimiento del packamn
    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)
    # revisar que el packman tenga un punto al comer las bolitas blancas!!
    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    #para que el packamn este sentrado!!
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')


# Movimiento de los fantasmas
    for point, course, color in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            #######################################################################################################################################
            #verifica posicion del packman y hace que los fantasmas se muevan hacia el dependiendo del cuadrante que se encuentra el packman!!    #
            #utilizando if statemnts y elif para poder validar esto!!                                                                             #
            #verificando las cordenadas de pacman en 'x' y en 'y'                                                                                 #
            #######################################################################################################################################
            if pacman.x > point.x  and pacman.y > point.y:
                options = [
                    vector(0,10),
                    vector(10,0)
                ]
            elif pacman.x > point.x and pacman.y < point.y:
                options = [
                    vector(0,-10),
                    vector(10,0)
                ]

            elif pacman.x < point.x  and pacman.y > point.y:

                options = [
                    vector(0,10),
                    vector(-10,0)
                ]

            elif pacman.x < point.x  and pacman.y < point.y:

                options = [
                    vector(0,-10),
                    vector(-10,0)
                ]

            elif pacman.x == point.x:

                options = [
                    vector(0,-10),
                    vector(0,10)
                ]


            elif pacman.y == point.y:

                options = [
                    vector(10,0),
                    vector(-10,0)
                ]

            else:

                options = [
                    vector(5, 0),
                    vector(-5, 0),
                    vector(0, 5),
                    vector(0, -5),
                ]

            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, color)

    update()

    for point, course, color in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)
#verificar que la dirreccion del packman puede ser valid!!
def change(x, y):
    "Change pacman aim if valid."
    #if statemnts para poder ver que si puede estar en esa posicion el packman!!
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

